package net.ddns.endercrypt.webwindowlink.exception;

public class MissingDesktopSupport extends RuntimeException
{

	public MissingDesktopSupport()
	{
	}

	public MissingDesktopSupport(String message)
	{
		super(message);
	}

	public MissingDesktopSupport(Throwable cause)
	{
		super(cause);
	}

	public MissingDesktopSupport(String message, Throwable cause)
	{
		super(message, cause);
	}

	public MissingDesktopSupport(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace)
	{
		super(message, cause, enableSuppression, writableStackTrace);
	}

}
